public class Main {
    public static void main(String[] args) {

       char harf='A';

       switch (harf){
           case 'a':
           case 'e':
           case 'i':
           case 'o':
           case 'u':
           case 'A':
           case 'E':
           case 'I':
           case 'O':
           case 'U':
               System.out.println("Vowels");
               break;
               default:
                   System.out.println("Not Vowels");

       }
    }
}